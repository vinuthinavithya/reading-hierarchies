<section class="container section-space">
    <div class="row justify-content-center">
        <div class="col-md-10" data-aos="fade-up">
            <div class="info-card">
                <h3>Key Height Regulations in Kandy</h3>

                <ul class="rule-list mt-4">
                    <li>
                        <strong>Maximum Height Limit:</strong><br>
                        In designated commercial and heritage zones, building height is generally capped
                        at <strong>12 meters</strong> (measured from ground level to roof top), particularly
                        near the Temple of the Tooth Relic.
                    </li>

                    <li>
                        <strong>Heritage Area Restrictions:</strong><br>
                        Height controls are strict within the Heritage Area to preserve historic character.
                        Limited relaxation may apply in zones located further away from the Temple.
                    </li>

                    <li>
                        <strong>Roof Regulations:</strong><br>
                        Buildings are required to use <strong>pitched roofs with tiles</strong>, maintaining
                        a minimum slope of <strong>22.5 degrees</strong> to harmonize with traditional
                        Kandyan architecture.
                    </li>

                    <li>
                        <strong>Building Categories:</strong><br>
                        Structures are classified into Categories <strong>A, B, and C</strong>.
                        Category A buildings (5 or more floors or over 15m in height) require
                        specialized planning approval and stricter regulation.
                    </li>

                    <li>
                        <strong>Floor Height Standards:</strong><br>
                        Minimum habitable floor height is set at <strong>2.8m (average)</strong> and
                        <strong>2.4m (minimum)</strong> for residential spaces, while commercial ground
                        floors require a minimum height of <strong>3.0m</strong>.
                    </li>
                </ul>
            </div>
        </div>
    </div>
